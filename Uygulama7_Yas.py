yas = int(input('yasınızı giriniz'))
if yas < 14:
    print('Merhaba Çocuk')
if 14 <= yas < 18:
    print('Merhaba Genç')
if 18 <= yas < 24:
    print('Merhaba Üniversiteli')
if 24 <= yas < 30:
    print('Evlenme Vakti')
if 30 <= yas < 50:
    print('Çoluk Çocuk Nasıl')
if yas >= 50:
    print('Hayatın Son demleri')